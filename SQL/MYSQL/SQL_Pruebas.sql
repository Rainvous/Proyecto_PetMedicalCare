-- SELECT PARA ROLES USUARIOS
SELECT 
    u.USUARIO_ID,
    u.USENAME AS Usuario,
    r.NOMBRE AS Rol
FROM USUARIOS u
INNER JOIN ROLES_USUARIO ru ON u.USUARIO_ID = ru.USUARIO_ID
INNER JOIN ROLES r ON ru.ROL_ID = r.ROL_ID
ORDER BY u.USUARIO_ID, r.NOMBRE;




